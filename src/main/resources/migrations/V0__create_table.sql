CREATE TABLE CARD (
    CARD_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    CARD_NUMBER VARCHAR2(80) NOT NULL,
    SECURITY_NUMBER VARCHAR2(3) NOT NULL,
    EXPIRATION_DATE VARCHAR2(7) NOT NULL,
    BALANCE NUMBER(15,2) NOT NULL,
    CLIENT_ID NUMBER NOT NULL,
    CARD_CLIENT_NAME VARCHAR2(120) NOT NULL,
    COMPANY_ID NUMBER NOT NULL,
    CARD_COMPANY_NAME VARCHAR2(120) NOT NULL
);